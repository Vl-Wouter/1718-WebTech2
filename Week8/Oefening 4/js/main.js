function promptGeo() {
    if("geolocation" in navigator) {
        navigator.geolocation.getCurrentPosition(function(position) {
            initMap(position.coords.latitude, position.coords.longitude);
        });
    }
    else {
        initMap(50.850346, 4.351721);
    }
}

